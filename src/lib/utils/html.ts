/**
 * Escapes HTML special characters to prevent XSS/HTML injection
 * Use this for any user-provided data inserted into HTML templates
 */
export function escapeHtml(unsafe: string): string {
  return unsafe
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;')
}
